import { readdir, readFile, stat, writeFile, mkdir } from 'node:fs/promises';
import path from 'node:path';

const root = process.cwd();
const generatedDir = path.join(root, 'docs', 'harness', 'generated');
const scanRoots = ['apps', 'packages', 'scripts'];
const interestingExtensions = new Set(['.ts', '.tsx', '.js', '.mjs', '.json', '.md']);

async function exists(filePath) {
  try {
    await stat(filePath);
    return true;
  } catch {
    return false;
  }
}

async function walk(dir) {
  if (!(await exists(dir))) {
    return [];
  }

  const entries = await readdir(dir, { withFileTypes: true });
  const files = await Promise.all(
    entries
      .filter(
        (entry) =>
          !['node_modules', '.next', 'dist', 'coverage', '.git', 'generated'].includes(entry.name),
      )
      .map(async (entry) => {
        const fullPath = path.join(dir, entry.name);
        if (entry.isDirectory()) {
          return walk(fullPath);
        }

        const ext = path.extname(entry.name);
        return interestingExtensions.has(ext) ? [fullPath] : [];
      }),
  );

  return files.flat();
}

function toRepoPath(filePath) {
  return path.relative(root, filePath).replaceAll(path.sep, '/');
}

async function getPackageSummary(packageJsonPath) {
  const raw = await readFile(packageJsonPath, 'utf8');
  const parsed = JSON.parse(raw);
  return {
    name: parsed.name ?? path.basename(path.dirname(packageJsonPath)),
    scripts: Object.keys(parsed.scripts ?? {}),
    dependencies: Object.keys(parsed.dependencies ?? {}),
  };
}

async function buildCodeMap(files) {
  const byRoot = new Map();
  for (const file of files) {
    const repoPath = toRepoPath(file);
    const [top, second] = repoPath.split('/');
    const key = second ? `${top}/${second}` : top;
    const items = byRoot.get(key) ?? [];
    items.push(repoPath);
    byRoot.set(key, items);
  }

  const sections = [...byRoot.entries()]
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([name, paths]) => {
      const list = paths
        .sort((a, b) => a.localeCompare(b))
        .map((repoPath) => `- \`${repoPath}\``)
        .join('\n');
      return `## ${name}\n\n${list}`;
    });

  return `# Harness Code Map

Generated by \`npm run docs:harness:update\`.

${sections.join('\n\n')}
`;
}

async function buildWorkspaceSummary() {
  const packageFiles = (await walk(path.join(root, '.'))).filter((file) =>
    file.endsWith('package.json'),
  );
  const summaries = await Promise.all(packageFiles.map(getPackageSummary));
  const rows = summaries
    .sort((a, b) => a.name.localeCompare(b.name))
    .map(
      (summary) =>
        `| \`${summary.name}\` | ${summary.scripts.join(', ') || '-'} | ${
          summary.dependencies.length
        } runtime deps |`,
    )
    .join('\n');

  return `# Workspace Summary

Generated by \`npm run docs:harness:update\`.

| Package | Scripts | Dependencies |
| --- | --- | --- |
${rows}
`;
}

await mkdir(generatedDir, { recursive: true });

const files = (
  await Promise.all(scanRoots.map((scanRoot) => walk(path.join(root, scanRoot))))
).flat();

await writeFile(path.join(generatedDir, 'code-map.md'), await buildCodeMap(files));
await writeFile(path.join(generatedDir, 'workspace-summary.md'), await buildWorkspaceSummary());
